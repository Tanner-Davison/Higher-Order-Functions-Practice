///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
                                        // what .map is actually doing //
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function mapMethod(array, callback){
    let newArray = []
    for(i=0;i<array.length;i++){
        newArray[i] = callback(array[i]);
    }
    return newArray
}
let newArrayOutside = mapMethod(array,function(num){
    return num +1;
})

console.log(newArrayOutside)


